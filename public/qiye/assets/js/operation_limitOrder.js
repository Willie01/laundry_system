var app=angular.module("ordersApp",[]);
app.controller("ordersCtr",["$scope","$http",function($scope,$http){
	var all_url="http://www.tchautchau.cn";
	/*显示所有信息*/
	$scope.show_list=function(){
		$http.get(all_url+"/api/orders").success(function(data){
			console.log(data)
			$scope.orders_list=data;
		});
	}
	$scope.show_list();
	/*显示类型*/
	$scope.show_model=function(){
		$(".model-show").show();
		$(".change_save").hide();
		$(".save").show();
	}
	/*隐藏*/
	$scope.hide_model=function(){
		$(".model-show").hide();
		$scope.category_name='';
		$scope.category_status='';
		$scope.category_city='';
	}
	/*添加*/
	$scope.add_orders=function(){
		$http({
			method:'POST',			
			url:all_url+"/api/orders",
			data:$.param({
				category_name:$scope.category_name,
				status:$scope.category_status,
				city:1
			}),
			headers:{"Content-Type":"application/x-www-form-urlencoded"} 
		}).success(function(data){
			$(".model-show").hide();
			$scope.show_list();
		}).error(function(){
			console.log("错误");
		})
	}
	/*显示某个品类*/
	$scope.edit_orders=function(id){
		$http({
			method:'GET',
			url:all_url+"/api/orders/"+id,
		}).success(function(data){
			$(".model-show").show();
			$(".change_save").show();
			$(".save").hide();
			$scope.category_name=data.category_name;
			$scope.category_status=data.status;
			$scope.category_city=data.city_name
		})
		$scope.change_orders=function(){
			$http({
				method:'POST',			
				url:all_url+"/api/update/orders/"+id,
				data:$.param({
					category_name:$scope.category_name,
					status:$scope.category_status,
					city:1
				}),
				headers:{"Content-Type":"application/x-www-form-urlencoded"} 
			}).success(function(data){
				$(".model-show").hide();
				$scope.show_list();
			}).error(function(){
				console.log("错误");
			})
		}
	}
	$scope.delete_orders=function(e){
		orders_id=$(e.target).attr("orders_id");
		console.log(orders_id)
		$http({
			method:'POST',
			url:all_url+'/api/delete/orders/'+orders_id,
			data:$.param({}),
			headers:{"Content-Type":"application/x-www-form-urlencoded"}
		}).success(function(data){
			orders_id=0;
			$scope.show_list();
		}).error(function(){})
	}
}])