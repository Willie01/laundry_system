var app=angular.module("sortApp",[]);
app.controller("sortCtr",["$scope","$http",function($scope,$http){
	var all_url="http://www.tchautchau.cn";
	/*显示所有信息*/
	$scope.show_list=function(){
		$http.get(all_url+"/api/categories").success(function(data){
			$scope.sort_list=data;
		});
	}
	$scope.show_list();
	/*显示类型*/
	$scope.show_model=function(){
		$(".model-show").show();
		$(".change_save").hide();
		$(".save").show();
	}
	$(".model-show-edit").hide();
	/*隐藏*/
	$scope.hide_model=function(){
		$(".model-show").hide();
		$(".model-show-edit").hide();
		$scope.category_name='';
		$scope.category_status='';
		$scope.category_city='';
	}
	/*添加*/
	$scope.add_sorts=function(){
		$http({
			method:'POST',			
			url:all_url+"/api/categories",
			data:$.param({
				category_name:$scope.category_name,
				status:$scope.category_status,
				city:1
			}),
			headers:{"Content-Type":"application/x-www-form-urlencoded"} 
		}).success(function(data){
			$(".model-show").hide();
			$scope.show_list();
		}).error(function(){
			console.log("错误");
		})
	}
	/*显示某个品类*/
	$scope.edit_sorts=function(id){
		$http({
			method:'GET',
			url:all_url+"/api/categories/"+id,
		}).success(function(data){
			$(".model-show-edit").show();
			$(".change_save").show();
			$(".save").hide();
			$scope.category_name=data.category_name;
			$scope.category_status=data.status;
			$scope.category_city=data.city_name
		})
		$scope.change_sorts=function(){
			console.log($(".sort").val())
			$http({
				method:'POST',			
				url:all_url+"/api/update/categories/"+id,
				data:$.param({					
					category_name:$(".sort").val(),
					status:$scope.category_status,
					city:1
				}),
				headers:{"Content-Type":"application/x-www-form-urlencoded"} 
			}).success(function(data){
				$(".model-show").hide();
				$(".model-show-edit").hide();
				$scope.show_list();
			}).error(function(){
				console.log("错误");
			})
		}
	}
	$scope.delete_sorts=function(e){
		sorts_id=$(e.target).attr("sorts_id");
		console.log(sorts_id)
		$http({
			method:'POST',
			url:all_url+'/api/delete/categories/'+sorts_id,
			data:$.param({}),
			headers:{"Content-Type":"application/x-www-form-urlencoded"}
		}).success(function(data){
			sorts_id=0;
			$scope.show_list();
		}).error(function(){})
	}
}])