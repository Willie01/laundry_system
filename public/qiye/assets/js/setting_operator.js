var app=angular.module("operatorsApp",[]);
app.controller("operatorsCtr",["$scope","$http",function($scope,$http){
	var all_url="http://www.tchautchau.cn";
	/*显示所有信息*/
	$scope.show_list=function(){
		$http.get(all_url+"/api/operators").success(function(data){
			$scope.operators_list=data;
		});
	}
	$scope.show_list();
	/*显示类型*/
	$scope.show_model=function(){
		$(".model-show").show();
		$(".change_save").hide();
		$(".save").show();
	}
	$(".model-show-edit").hide();
	/*隐藏*/
	$scope.hide_model=function(){
		$(".model-show").hide();
		$(".model-show-edit").hide();
		$scope.operator_name='';
		$scope.telphone='';
		$scope.password='';
		$scope.status='';
	}
	/*添加*/
	$scope.add_operators=function(){
		$http({
			method:'POST',			
			url:all_url+"/api/operators",
			data:$.param({
				operator_name:$scope.operator_name,
				telphone:$scope.telphone,
				password:$scope.password,
				status:$scope.status
				
			}),
			headers:{"Content-Type":"application/x-www-form-urlencoded"} 
		}).success(function(data){
			$(".model-show").hide();
			$scope.show_list();
		}).error(function(){
			console.log("错误");
		})
	}
	/*显示某个品类*/
	$scope.edit_operators=function(id){
		$http({
			method:'GET',
			url:all_url+"/api/operators/"+id,
		}).success(function(data){
			$(".model-show").show();
			$(".change_save").show();
			$(".save").hide();
			$scope.operator_name=data.operator_name;
			$scope.status=data.status;
			$scope.telphone=data.telphone;
			$scope.password=data.password;
		})
		$scope.change_operators=function(){
			$http({
				method:'POST',			
				url:all_url+"/api/update/operators/"+id,
				data:$.param({					
					operator_name:$scope.operator_name,
					telphone:$scope.telphone,
					password:$scope.password,
					status:$scope.status
				}),
				headers:{"Content-Type":"application/x-www-form-urlencoded"} 
			}).success(function(data){
				$(".model-show").hide();
				$scope.show_list();
			}).error(function(){
				console.log("错误");
			})
		}
	}
	$scope.delete_operators=function(e){
		operators_id=$(e.target).attr("operators_id");
		console.log(operators_id)
		$http({
			method:'POST',
			url:all_url+'/api/delete/operators/'+operators_id,
			data:$.param({}),
			headers:{"Content-Type":"application/x-www-form-urlencoded"}
		}).success(function(data){
			operators_id=0;
			$scope.show_list();
		}).error(function(){})
	}
}])